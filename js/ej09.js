// 9. Escribe un programa que solicite al usuario ingresar un número y determine si es positivo,
// negativo o cero.

let number = prompt('Ingrese un número porfavor.');

if (number == 0) {
    alert('El número introducido es 0.');

} else if (number > 0) {
    alert('El número introducido es positivo.');

} else {
    alert('El número introducido es negativo.');
    
}