// 13. Realiza un programa que solicite al usuario ingresar dos números y determine si son iguales
// o diferentes.

let number1 = prompt('Porfavor ingrese el primer número.');
let number2 = prompt('Porfavor ingrese el segundo número.');

if (number1 == number2) {
    alert('Los números introducidos son iguales');

} else {
    alert('Los números introducidos NO son iguales');
}