// 11. Crea un programa que solicite al usuario ingresar un número y determine si es divisible
// entre 3.

let number = prompt('Introduzca un numero porfavor');

if (number % 3 == 0) {
    alert('El número ' + number + ' es divisible entre 3');

} else {
    alert('El número ' + number + ' NO es divisible entre 3');

}